function stripPeriod(text) {
  return String(text || "").replace(/[.،؛\s]+$/u, "");
}

function pickProfile(service) {
  return serviceExperienceProfiles[service.category] || serviceExperienceProfiles.all;
}

function buildDeliverableText(outcome, index, service, profile) {
  const cleanOutcome = stripPeriod(outcome);
  const templates = [
    `نحوّل "${cleanOutcome}" إلى جزء واضح داخل ملف ${service.title}، مع شرح ما يعنيه للعميل وما الذي يحتاجه قبل البدء.`,
    `يتم ترتيب "${cleanOutcome}" بطريقة تساعد العميل على المقارنة واتخاذ قرار عملي، بدل أن تبقى المعلومة متفرقة بين أكثر من جهة.`,
    `نربط "${cleanOutcome}" بالمتطلبات والبيانات المتاحة، ثم نوضح أثرها على المسار ${profile.lens} للمشروع.`,
    `نلخص "${cleanOutcome}" في توصية قابلة للتنفيذ، بحيث يعرف العميل الأولوية والخطوة التالية بدون غموض.`
  ];

  return templates[index % templates.length];
}

function buildWorkflowText(step, index, service, profile) {
  const cleanStep = stripPeriod(step);
  const templates = [
    `في هذه المرحلة نبدأ بـ "${cleanStep}" حتى نفهم وضع العميل بدقة ونبني الخدمة على معلومات صحيحة.`,
    `بعد ذلك نعمل على "${cleanStep}" ونحوّل البيانات إلى صورة مرتبة يسهل عرضها على الإدارة أو الشركاء.`,
    `ثم نراجع "${cleanStep}" من زاوية ${profile.lens} للتأكد أن التوصية مناسبة لطبيعة المشروع وليست نسخة عامة.`,
    `وأخيرًا يتم "${cleanStep}" في شكل مخرجات واضحة تساعد العميل على المتابعة أو التواصل مع الجهة التالية بثقة.`
  ];

  return templates[index % templates.length];
}

